package com.example.earthquake_inspector_app;

public class FormaProtovathmias {

    String sintetagmenes, dimos, odos, arithmos,
            perioxi, onoma, eponimo, tilefono, arithmos_orofon,
            arithmos_diamerismaton, apofasi_katalilotitas, tipos_ktiriou;

    public FormaProtovathmias() {
    }

    public FormaProtovathmias(String sintetagmenes, String dimos, String odos, String arithmos,
                              String perioxi, String onoma, String eponimo, String tilefono,
                              String arithmos_orofon, String arithmos_diamerismaton, String apofasi_katalilotitas,
                              String tipos_ktiriou) {
        this.sintetagmenes = sintetagmenes;
        this.dimos = dimos;
        this.odos = odos;
        this.arithmos = arithmos;
        this.perioxi = perioxi;
        this.onoma = onoma;
        this.eponimo = eponimo;
        this.tilefono = tilefono;
        this.arithmos_orofon = arithmos_orofon;
        this.arithmos_diamerismaton = arithmos_diamerismaton;
        this.apofasi_katalilotitas = apofasi_katalilotitas;
        this.tipos_ktiriou = tipos_ktiriou;
    }

    public String getSintetagmenes() {
        return sintetagmenes;
    }

    public String getDimos() {
        return dimos;
    }

    public String getOdos() {
        return odos;
    }

    public String getArithmos() {
        return arithmos;
    }

    public String getPerioxi() {
        return perioxi;
    }

    public String getOnoma() {
        return onoma;
    }

    public String getEponimo() {
        return eponimo;
    }

    public String getTilefono() {
        return tilefono;
    }

    public String getArithmos_orofon() {
        return arithmos_orofon;
    }

    public String getArithmos_diamerismaton() {
        return arithmos_diamerismaton;
    }

    public String getApofasi_katalilotitas() {
        return apofasi_katalilotitas;
    }

    public String getTipos_ktiriou() {
        return tipos_ktiriou;
    }
}
